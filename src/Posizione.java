public class Posizione {
	
	private int riga;
	private int colonna;

	public Posizione(int riga, int colonna) {
		this.riga = riga;
		this.colonna = colonna;
	}
	/**
	 * 
	 * @return riga
	 */
	public int getRiga(){
		return riga;
	}
	/**
	 * 
	 * @return colonna
	 */
	public int getColonna(){
		return colonna;
	}
	
}
